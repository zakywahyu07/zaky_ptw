  <br><br><h2 style="text-align:center;">Pengenalan Apa Itu Framework dan Jenisnya untuk Web Development</h2>
  <img src="framework.jpg" width="480" height="280" align="centre"> 

  

  <p style="text-align:justify;">Framework (kerangka kerja) adalah istilah yang sering muncul dalam dunia developer. Istilah tersebut memiliki fungsi yang sangat besar bagi pengembangan kode program secara sistematis.<br> Saat ini, seorang pengembang khususnya dalam bidang web development diharuskan untuk mempelajari dan menggunakan sebuah kerangka kerja dalam pembuatan perangkat lunak.<br><br>

Keuntungan lain adalah untuk mengembangkan perangkat lunak dengan penyusunan kode secara terstruktur dan konsisten. Kode yang baik tentu saja merupakan kode yang dapat dimengerti oleh mesin serta pengembang (developer).<br><br>

Pada artikel kali ini, akan membahas seputar apa itu framework beserta jenis – jenisnya. Untuk jenis dari kerangka kerja -nya sendiri lebih menekankan pada web development. Kita akan membahas terlebih dahulu mengenai apa itu framework.
  </p>

  <br><h2 style="text-align:left;">Pengenalan Apa Itu Framework</h2>
  <p style="text-align:justify;">Pertama, kita harus mengenal terlebih dahulu apa itu framework dan fungsi – fungsi apa saja yang dimilikinya. Nah, dari situ anda dapat mulai memahami tujuan dari penggunaannya dalam pengembangan website ataupun perangkat lunak.
  </p>

  <br><h3 style="text-align:left;">1. Pengertian Framework</h3>
  <p style="text-align:justify;">Sesuai dengan namanya sendiri, framework adalah kerangka kerja untuk mengembangkan aplikasi berbasis website maupun desktop. Kerangka kerja disini sangat membantu developer dalam menuliskan sebuah dengan lebih terstruktur dan tersusun rapi.
Kerangka kerja diciptakan untuk mempermudah kinerja dari programmer. Sehingga, seorang programmer tidak perlu untuk menuliskan kode secara berulang – ulang. Karena di dalamnya sendiri anda hanya perlu menyusun komponen – komponen pemrograman saja.</p>

  <br><h3 style="text-align:left;">2. Fungsi Framework</h3>
  <p style="text-align:justify;">Sebagai developer, tentu harus mengetahui tujuan dari penggunaannya untuk kepentingan pembuatan aplikasi. Sehingga, untuk proses pengerjaan aplikasi dapat dilakukan dengan menggunakan framework yang tepat dan sesuai dengan kebutuhan project. Berikut merupakan beberapa fungsi kerangka kerja dalam web development.</p>

  <h4 style="text-align:left;">a. Kode program lebih terstruktur</h4>
  <p style="text-align:justify;">Fungsi framework yang utama adalah membuat source code menjadi lebih terstruktur. Terstruktur disini, berarti program yang dibuat akan dimasukkan ke dalam setiap komponen sesuai dengan fungsi nya masing – masing.<br><br>
Salah satu contoh dari kode program terstruktur dapat dilihat dari framework PHP, yaitu Laravel. Yang menggunakan konsep paradigma MVC (Model, View, Controller). Terdapat tiga komponen utama untuk mengembangkan website menggunakan model framework tersebut.<br><br>
Model berfungsi untuk tempat atau wadah menampung kode program berupa algoritma pemrograman dan penghubung database aplikasi. View berfungsi sebagai wadah menampung kode program untuk membuat tampilan yang nantinya ditampilkan kepada customer / client. Dan controller berfungsi untuk menghubungkan model dan view agar menjadi sebuah website secara keseluruhan.<br><br>
Dapat dikatakan juga, model disini untuk menangani tugas back end. Kemudian, view untuk menangani tugas front end. Dengan adanya konsep MVC tersebut, kode program akan tersusun rapi serta mempersingkat kerja dari developer.</p>

  <h4 style="text-align:left;">b. Membantu kinerja dari developer</h4>
  <p style="text-align:justify;">Fungsi yang kedua adalah membantu kinerja dari developer sendiri. Dari sini, anda pasti sudah berpikir bahwa sebenarnya dalam membuat sebuah aplikasi dapat dilakukan tanpa menggunakan bantuan framework. Tentu saja, bisa dilakukan jika proyek yang ditangani dalam lingkup kecil.<br><br>
Apabila anda membuat aplikasi atau tampilan website untuk sebuah perusahaan atau organisasi besar tentu saja hal tersebut tidak disarankan bahkan mindset tersebut harus segera diubah. Framework diciptakan untuk memudahkan kinerja dari developer dalam segi efisiensi waktu serta resource yang dibutuhkan.<br><br>
Selain itu, apabila dikerjakan oleh beberapa tim maka framework akan sangat membantu efektivitas kerja dan sinkronisasi. Setiap dapat langsung memahami dan mengerjakan tugas masing – masing tanpa perlu mengecek satu persatu komponen baik dari segi back end maupun front end.
</p>

  <h4 style="text-align:left;">c. Meningkatkan keamanan perangkat lunak / website</h4>
  <p style="text-align:justify;">Selain membantu kinerja dari developer, dari segi keamanan website maupun software juga akan meningkat. Karena dalam framework telah diidentifikasi oleh beberapa developer ahli dari berbagai negara.<br><br>
Kemudian, sebuah kerangka kerja juga memiliki beberapa versi yang setiap saat akan selalu ada pembaruan dari segi fitur serta untuk mengurangi adanya bugs. Keamanan akan selalu diperbarui dan dimonitoring agar tidak terjadi permasalahan yang lebih serius pada website misalnya terjadinya hacking maupun peretasan data.<br><br>
Apabila anda tertarik dengan sistem keamanan aplikasi, maka anda juga dapat untuk belajar cyber security. Beberapa perusahaan maupun startup memiliki beberapa posisi untuk mengerjakan dan menangani permasalahan dalam urusan sistem keamanan perangkat lunak.</p>

